def properties(pf):
    '''
    Save data into a properties file
    '''
    for pname, pval in _properties.iteritems():
        if isinstance(pval, tuple): # vector type
            pf.add(name,pname,zip(*pval))
        else:                   # scalar type
            pf.add(name,pname,[(0,pval)])
        continue
    return
